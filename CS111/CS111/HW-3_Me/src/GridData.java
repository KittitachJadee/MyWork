public class GridData {
	private boolean isOpen, hasBomb;
	
	public GridData() {
		isOpen = false;
		hasBomb = false;
	}
	
	public boolean getIsOpen() {
		return isOpen;
	}
	
	public void setIsOpen(boolean isOpen) {
		this.isOpen = isOpen;
	}
	
	public boolean getHasBomb() {
		return hasBomb;
	}
	
	public void setHasBomb(boolean hasBomb) {
		this.hasBomb = hasBomb;
	}
}
